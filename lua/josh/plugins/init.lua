        Util.telescope("live_grep"),
        desc = "[S]earch by [G]rep",
      },
      {
        "<leader>sd",
        Util.telescope("diagnostics"),
        desc = "[S]earch [D]iagnostics",
      },
      {
        "<leader>sr",
        Util.telescope("resume"),
        desc = "[S]earch [R]esume",
      },
    },
    opts = {
      defaults = {
        mappings = {
          i = {
            ['<C-u>'] = false,
            ['<C-d>'] = false,
          },
        },
      },
    },
